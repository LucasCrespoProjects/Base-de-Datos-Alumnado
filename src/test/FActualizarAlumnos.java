    private void NuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NuevoActionPerformed
        Alumnos newAlumno = new Alumnos();
        
//        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
//        dtf.format(LocalDateTime.now());
//        
//        String marca = String.valueOf(dtf);
        
        String id = DniIngresado.getText();
        String nombres = txtNombre.getText();
        String apellidos = TxtApellidos.getText();
        String FechaN = TxtNacimiento.getText();
        String Edad = TxtEdad.getText();
        String Direccion = TxtDireccion.getText();
        String PisoDepto = this.PisoDepto.getText();
        String Localidad = TxtLocalidad.getText();
        String Correo_Electronico = TxtEmail.getText();
        String Celular = TxtTelefono.getText();
        String Curso = CBXGrado.getSelectedItem().toString();
        String Division = CBXSeccion.getSelectedItem().toString();

        int id_Alumno = Integer.parseInt(id);
        int edad_Alumno = Integer.parseInt(Edad);
        
        newAlumno.setDNI(id_Alumno);
        newAlumno.setApellido(apellidos);
        newAlumno.setNombre(nombres);
        newAlumno.setFecha_N(FechaN);
        newAlumno.setEdad(edad_Alumno);
        newAlumno.setDireccion(Direccion);
        newAlumno.setPiso_Depto(PisoDepto);
        newAlumno.setLocalidad(Localidad);
        newAlumno.setCorreo(Correo_Electronico);
        newAlumno.setCelular(Celular);
        newAlumno.setCurso(Curso);
        newAlumno.setDivision(Division);

        lista_alumnos.update(newAlumno);

        JOptionPane.showMessageDialog(null, "Se modificado con exito");
        
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
      
    }//GEN-LAST:event_NuevoActionPerformed

