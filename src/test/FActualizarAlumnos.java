            txtMarca.setText(arr2.get(i).getMarca_Temporal());
            txtDni.setText(String.valueOf(arr2.get(i).getDNI()));
            txtNombre.setText(arr2.get(i).getNombre());
            TxtApellidos.setText(arr2.get(i).getApellido());
