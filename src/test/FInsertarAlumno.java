    private void NuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NuevoActionPerformed

        String timeStamp = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss").format(Calendar.getInstance().getTime());
        System.out.println(timeStamp);
        String id = TxtDni.getText();
        String apellido = TxtApellido.getText();
        String nombre = TxtNombre.getText();        
        String fecha = TxtFecha.getText();
        String e = TxtEdad.getText();
        String direccion = TxtDireccion.getText();
        String piso = TxtPiso.getText();
        String localidad = TxtLocalidad.getText();
        String correo = TxtCorreo.getText();
        String celular = TxtCelular.getText();
        String curso = CBXCurso.getSelectedItem().toString();
        String division = CBXSeccion.getSelectedItem().toString();
        int  dni = Integer.parseInt(id);
        int edad = Integer.parseInt(e);

        p3.setMarca_Temporal(timeStamp);
        p3.setDNI(dni);
        p3.setApellido(apellido);
        p3.setNombre(nombre);
        p3.setFecha_N(fecha);
        p3.setEdad(edad);
        p3.setDireccion(direccion);
        p3.setPiso_Depto(piso);
        p3.setLocalidad(localidad);
        p3.setCorreo(correo);
        p3.setCelular(celular);
        p3.setCurso(curso);
        p3.setDivision(division);

        lista_alumnos.insert(p3);

        JOptionPane.showMessageDialog(null, "Se insert√≥ el nuevo producto en la base de datos");

    }//GEN-LAST:event_NuevoActionPerformed

